    public void setPilihTiket(){
        HargaTiket = Integer.parseInt(pilihtiket[4].toString());

        lblAsal.setText(": " + pilihtiket[0].toString());
        lblTujuan.setText(": " + pilihtiket[1].toString());
        lblPenerbangan.setText(": " + pilihtiket[2].toString());
        lblPukul.setText(": " + pilihtiket[3].toString());
        lblHarga.setText(": " + ubahHarga(HargaTiket));
        
        lblTotalHarga.setText(ubahHarga(HargaTiket));
    }
    public String ubahHarga(int harga){
        DecimalFormat df = (DecimalFormat) DecimalFormat.getCurrencyInstance();
        DecimalFormatSymbols Rp = new DecimalFormatSymbols();
        
        Rp.setCurrencySymbol("Rp. ");
        Rp.setMonetaryDecimalSeparator(',');
        Rp.setGroupingSeparator('.');
        
        df.setDecimalFormatSymbols(Rp);
        return (df.format(harga));    
        
    }
    public void setHitung(){
        int JumlahPenumpang = jTablePenumpang.getRowCount();
//        JTableTiket.getModel().getValueAt(baris, i).toString();
        int Harga = HargaTiket;
        int TotalHarga = HargaTiket * JumlahPenumpang;
        
        lblJumlahPenumpang.setText(Integer.toString(JumlahPenumpang));
        lblTotalHarga.setText(ubahHarga(Harga));
        
        lblFinalHarga.setText(ubahHarga(TotalHarga));
    }
