    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        DefaultTableModel penumpang = (DefaultTableModel)jTablePenumpang.getModel();
        String nama_depan = txtNamaDepan.getText();
        String nama_belakang = txtNamaBelakang.getText();
        
        String fullname = nama_depan + " " + nama_belakang;
        String jk = "Laki - laki";
        if(!jRadioButton1.isSelected()){
            jk = "Perempuan";
        }
        String usia = cbUsia.getSelectedItem().toString();
        penumpang.addRow( new Object[] {fullname, nama_depan, nama_belakang, jk, usia});
        
        setHitung();
        resetForm();
    }//GEN-LAST:event_btnTambahActionPerformed

